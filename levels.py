def get_level(numb):
    if numb == -1:
        level = ['--------------',
                 '--          --',
                 '--          --',
                 '--          --',
                 '--          --',
                 '--           $',
                 '--           $',
                 '--------------']

    if numb == 0:
        level = ['-----------------------------------------------------------------------------------------------------------------------',
                 '--  ------          ---         --         ---                  ---      --      --      --               --         --',
                 '--  ------          ---         +          --                    --      --      --      --               --         --',
                 '--  ------   --     ---                    --          ^         --  --  --  --  --  --  --  --           --         --',
                 '--  ------   --     ---  ----  ----  ----  --                    --  --  --  --  --  --  --  --       --  --  --     --',
                 '--           --          --     --     --                            --  +   --      --  +   --    --*--      --*--   $',
                 '--           -- *        --  *     *   --         *  *   *  *        --      --      --      --    -----      -----   $',
                 '-----------------------------------------------------------------------------------------------------------------------']
    elif numb == 1:
        level = ['----------------------------------------------------------------------------------------------------------',
                 '--  --       -----       ---------                         -------  ---      --      --      --          -',
                 '--  --       ----         -------                ^          -----    --      --      --      --          -',
                 '--  --  --   ---           -----               ---------     ---     --  --  --  --  --  --  --  --      -',
                 '--  --  --   --     -*-     ---           --   -----------           --  --  --  --  --  --  --  --      -',
                 '--      --    +    -----     +       --        -------------             --  +   --      --  +   --      $',
                 '--      -- *      -------       --  *   *    * ---------------*          --      --      --      --*     $',
                 '----------------------------------------------------------------------------------------------------------']
    elif numb > 1:
        level = ['--------------',
                 '--          --',
                 '--++++++++++--',
                 '--          --',
                 '--++++++++++--',
                 '--          $-',
                 '--   ****   $-',
                 '--------------']
    return level
